package entities;

public class ComunicationEntity {
	public String user;
	public String password;
	public String jcoSysnr;
	public String jcoClient;
	public String server;
	public String destinationName;
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getJcoSysnr() {
		return jcoSysnr;
	}
	public void setJcoSysnr(String jcoSysnr) {
		this.jcoSysnr = jcoSysnr;
	}
	public String getJcoClient() {
		return jcoClient;
	}
	public void setJcoClient(String jcoClient) {
		this.jcoClient = jcoClient;
	}
	public String getServer() {
		return server;
	}
	public void setServer(String server) {
		this.server = server;
	}
	public String getDestinationName() {
		return destinationName;
	}
	public void setDestinationName(String destinationName) {
		this.destinationName = destinationName;
	}
	
	
}
